public class Aula16 {
    
    public static void main(String[] args) {
        
        /**
         * e, ou, não
         * and, or, not
         * &&, ||, !
         */

        int x = 6;

        System.out.println((x >= 1) && (x <= 10));

        System.out.println((x >= 1) || (x < 5));

        System.out.println(!(x >= 1));


    }
}
