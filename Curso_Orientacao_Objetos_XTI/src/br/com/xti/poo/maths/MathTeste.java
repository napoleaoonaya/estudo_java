package br.com.xti.poo.maths;

public class MathTeste {
	
	public static void main(String[] args) {
		
		System.out.println(Math.sin(10));//seno
		System.out.println(Math.cos(10));//coseno
		System.out.println(Math.tan(10));//tangente
		System.out.println(Math.sqrt(2));//raiz quadrada
		System.out.println(Math.pow(2,3));//potencia
		System.out.println(Math.cbrt(2));//raiz cubica
		System.out.println(Math.hypot(10, 10));//hipotenusa
		System.out.println(Math.log(10));//logaritmo
		System.out.println(Math.signum(10));// positivo 1, negativo -1, zero 0
		System.out.println(Math.ceil(10));
		System.out.println(Math.round(10));//arredonda
		System.out.println(Math.floor(10));//traz sem arredondar
		System.out.println(Math.min(10,20));//minimo
		System.out.println(Math.max(10,30));//maximo
		
	}

}
