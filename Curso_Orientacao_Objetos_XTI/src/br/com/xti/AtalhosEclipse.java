package br.com.xti;

import java.util.ArrayList;

public class AtalhosEclipse {

	public static void main(String[] args) {

		ArrayList<String> nomes = new ArrayList<String>();

		nomes.add("Miguel");
		nomes.add("João");
		nomes.add("Lucas");
		nomes.add("Mateus");

		for (String n : nomes) {
			System.out.println(n.toString());
		}

		/**
		 * Correção de erros ctrl+1
		 * Autocomplete e dicas ctrl+space
		 * Import ctrl+shift+o
		 * Formatar código ctrl+shift+f
		 * Navegação ctrl+o
		 * Comentar ctrl+/
		 * 
		 * Para encapsulamentos ctrl+3 depois digite ggas
		 * Para System.out.println digite sysou ctrl+space
		 * Para método main ctrl+space selecione main method
		 */
		
	}

}
